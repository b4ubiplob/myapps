package sampledbwebapp;

public class FirstTO {
	
	public FirstTO(String str, int num) {
		super();
		this.str = str;
		this.num = num;
	}
	private String str;
	private int num;
	
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	

}
